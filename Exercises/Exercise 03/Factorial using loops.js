let fac = 1; // Declared fac and assigned its value of 1
let i = 1; // Declared value of i to 1
let a = 6; // Declared value of a which to be declared here for the factorial calculation..........

while(i<=a){
    fac = fac * i;
    i++; // Increasing the value of i and checking till it is equal to value of a
}
console.log(fac); // Outputting the value of factorial